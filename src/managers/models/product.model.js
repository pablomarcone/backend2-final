import mongoose from 'mongoose';
import mongoosePaginate from 'mongoose-paginate-v2';

export const productModel = mongoose.model(
    'Product', new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    code: {
        type: String,
        required: true,
        unique: true
    },
    price: {
        type: Number,
        required: true
    },
    status: {
        type: Boolean,
        required: true
    },
    stock: {
        type: Number,
        required: true
    },
    category: {
        type: String,
        required: true
    },
    thumbnails: {
        type: [String],
        required: true
    },
}, { timestamps: true }).plugin(mongoosePaginate)
);